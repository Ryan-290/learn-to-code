package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 290 ; // 정수 타입
        double b = 10.5 ; // 실수 타입
        boolean c = true ; // True or false 2가지만 입력
        char d = 'A' ; // 문자 하나
        String e = "안녕 자바" ;// 문자열을 다루기 위한 특별한 타입;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
